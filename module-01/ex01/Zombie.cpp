#include "Zombie.hpp"
#include <iostream>

Zombie::Zombie(void)
{
	_name = "Unknown Zombie";
}

Zombie::~Zombie(void)
{
	std::cout << _name << ": am dead" << std::endl;
}

void	Zombie::setName(std::string name)
{
	_name = name;
}

void	Zombie::announce(void)
{
	std::cout << _name << ": BraiiiiiiinnnzzzZ..." << std::endl;
}
